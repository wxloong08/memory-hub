const initialLocale = (() => {
  if (typeof window === 'undefined') {
    return DEFAULT_LOCALE
  }

  const saved = window.localStorage.getItem(STORAGE_KEY)
  return saved && messages[saved] ? saved : DEFAULT_LOCALE
})()

const locale = ref(initialLocale)

function setLocale(nextLocale) {
  const normalized = messages[nextLocale] ? nextLocale : DEFAULT_LOCALE
  locale.value = normalized
  if (typeof window !== 'undefined') {
    window.localStorage.setItem(STORAGE_KEY, normalized)
    document.documentElement.lang = normalized === 'en' ? 'en' : 'zh-CN'
  }
}

function interpolate(template, params = {}) {
  return String(template || '').replace(/\{(\w+)\}/g, (_, key) => String(params[key] ?? ''))
}

export function useI18n() {
  const currentMessages = computed(() => messages[locale.value] || messages[DEFAULT_LOCALE])

  function t(key, params) {
    const value = currentMessages.value[key] ?? messages[DEFAULT_LOCALE][key] ?? key
    return interpolate(value, params)
  }

  function formatDateTime(value) {
    if (!value) {
      return ''
    }

    const date = new Date(value)
    if (isNaN(date.getTime())) {
      return String(value)
    }
    return date.toLocaleString(locale.value === 'en' ? 'en-US' : 'zh-CN', {
      year: 'numeric',
      month: '2-digit',
      day: '2-digit',
      hour: '2-digit',
      minute: '2-digit',
    })
  }

  return {
    locale,
    setLocale,
    t,
    formatDateTime,
  }
}
